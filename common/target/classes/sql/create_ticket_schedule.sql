CREATE TABLE `ticket_schedule` (
	`ts_id` int(11) NOT NULL AUTO_INCREMENT COMMENT '计划表ID',
	`ts_ticket_id` int(11) NOT NULL COMMENT '票的id',
	`ts_time` datetime NOT NULL COMMENT '场次，日期、时间',
	`ts_hall_id` int(11) DEFAULT NULL COMMENT '对应大厅id',
	`ts_start_time` datetime NOT NULL COMMENT '开始时间',
	`ts_end_time` datetime DEFAULT NULL COMMENT '结束时间',
	`ts_presale_start` datetime DEFAULT NULL COMMENT '预售开始时间',
	`ts_presale_end` datetime DEFAULT NULL COMMENT '预售结束时间',
	`ts_sale_start` datetime NOT NULL COMMENT '正常售票开始时间',
	`ts_sale_end` datetime NOT NULL COMMENT '正常售票结束时间',
	`ts_ticket_total` int(11) NOT NULL COMMENT '票的总数',
	`ts_presale_total` int(11) NOT NULL DEFAULT '0' COMMENT '预售票总数',
	`ts_ticket_stock` int(11) NOT NULL COMMENT '票的总库存',
	`ts_presale_stock` int(11) NOT NULL DEFAULT '0' COMMENT '预售票库存',
	`ts_available` int(11) NOT NULL DEFAULT '0' COMMENT '场次安排是否开启，默认为0未开启，1为开启，2为取消',
	`ts_create_time` datetime NOT NULL COMMENT '创建时间',
	`ts_update_time` datetime DEFAULT NULL COMMENT '更新时间',
  	PRIMARY KEY (`ts_id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 COMMENT='计划表'
